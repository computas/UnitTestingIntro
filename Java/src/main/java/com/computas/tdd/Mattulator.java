package com.computas.tdd;

public class Mattulator {
	public double adder(final double tall1, final double tall2) {
		return tall1 + tall2;
	}

	public double subtraher(final double tall1, final double tall2) {
		return tall1 - tall2;
	}

	public double multipliser(final double tall1, final double tall2) {
		return tall1 * tall2;
	}

	public double divider(final double teller, final double nevner) {
		return nevner / teller;
	}
}
